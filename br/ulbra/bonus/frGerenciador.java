    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        btnListar.doClick();
        int cod;
        cod = Integer.parseInt(JOptionPane.showInputDialog(null, "Informe o código abaixo:"));
        if (JOptionPane.showConfirmDialog(null, "tem certeza de que deseja deletar?", "Confirme:",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            gn.excluir(cod - 1);
        } else {
            JOptionPane.showMessageDialog(null, "Exclusão cancelada com sucesso!");
        }
        btnListar.doClick();
    }//GEN-LAST:event_btnDeletarActionPerformed

