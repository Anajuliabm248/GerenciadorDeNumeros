    private void btnAdiconarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdiconarActionPerformed

        int add = Integer.parseInt(txtNovoNumero.getText());

        if (txtNovoNumero.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Insira o número, por favor.");
        } else {
            gn.adicionar(add);
            JOptionPane.showMessageDialog(null, "Número adicionado com sucesso!");
            txtNovoNumero.requestFocus();
            txtNovoNumero.setText(null);
            btnListar.doClick();
        }

        //total de elementos
        int tamanho = gn.numero.size();
        String total = Integer.toString(tamanho);
        lbTotal1.setText(total);

        //media
        DecimalFormat f = new DecimalFormat("0.0");
        String n = f.format(gn.calcularMedia());
        lbMedia.setText(n);

        //max
        int max = Collections.max(gn.numero);
        String maior = Integer.toString(max);
        lbMax.setText(maior);

        //min
        int min = Collections.min(gn.numero);
        String menor = Integer.toString(min);
        lbMin.setText(menor);

    }//GEN-LAST:event_btnAdiconarActionPerformed

