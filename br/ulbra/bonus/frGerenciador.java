    private void btnDeletar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletar1ActionPerformed
        if (JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir tudo?", "Confirme:",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            gn.numero.clear();
            lbTotal1.setText("0");
            lbMedia.setText("0");
            lbMax.setText("0");
            lbMin.setText("0");
        } else {
            JOptionPane.showMessageDialog(null, "Exclusão cancelada com sucesso!");
        }
        btnListar.doClick();
    }//GEN-LAST:event_btnDeletar1ActionPerformed

    private void txtBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtBuscarActionPerformed
        int n = Integer.parseInt(txtBuscar.getText());

        int contagem = Collections.frequency(gn.numero, n);

        if (txtBuscar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Insira um número para pesquisa.");
        } else {
            JOptionPane.showMessageDialog(null, "O número: " + n
                    + " está presente na lista " + contagem + " vezes");
        }

    }//GEN-LAST:event_txtBuscarActionPerformed

